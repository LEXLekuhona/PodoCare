export function consentDocumentTitleRu(type: string): string {
  switch (type) {
    case 'PERSONAL_DATA':
      return 'Согласие на обработку персональных данных';
    case 'MEDICAL_INFORMATION':
      return 'Информированное согласие на медицинское вмешательство';
    case 'PROCEDURE_CONSENT':
      return 'Согласие на медицинское вмешательство';
    case 'MARKETING_COMMUNICATIONS':
      return 'Согласие на маркетинговые коммуникации';
    case 'TERMS_OF_SERVICE':
      return 'Условия использования сервиса';
    default:
      return 'Документ';
  }
}

export function getConsentParagraphs(type: string): string[] | null {
  switch (type) {
    case 'PERSONAL_DATA':
      return getPersonalDataParagraphs();
    case 'MEDICAL_INFORMATION':
      return getMedicalInformationParagraphs();
    default:
      return null;
  }
}
